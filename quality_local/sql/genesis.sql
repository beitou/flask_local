create table if not exists genesis(
  id int auto_increment primary key not null,
  button varchar(48) not null comment '页面显示的按钮',
  name varchar(48) not null comment '用户名',
  sex varchar(48) not null comment '性别',
  beneficiary varchar(48) not null comment '受益人',
  son varchar(48) not null comment '几个儿子',
  birthday varchar(48) not null comment '本人的生日',
  spouse_birthday varchar(48) not null comment '配偶的生日',
  father_birthday varchar(48) not null comment '父亲的生日',
  mother_birthday varchar(48) not null comment '母亲的生日',
  son1_birthday varchar(48) not null comment '儿子1的生日',
  son2_birthday varchar(48) not null comment '儿子1的生日',
  son3_birthday varchar(48) not null comment '儿子1的生日',
  security varchar(48) not null comment '本人的社保',
  spouse_security varchar(48) not null comment '配偶的社保',
  father_security varchar(48) not null comment '父亲的社保',
  mother_security varchar(48) not null comment '母亲的社保',
  son1_security varchar(48) not null comment '儿子1的社保',
  son2_security varchar(48) not null comment '儿子1的社保',
  son3_security varchar(48) not null comment '儿子1的社保',
  income varchar(48) not null comment '本人的收入',
  spouse_income varchar(48) not null comment '配偶的收入',
  housing_loan varchar(48) not null comment '家庭房屋贷款',
  loan_duration varchar(48) not null comment '放贷剩余年限',
  location varchar(48) not null comment '地区码表',
  demand varchar(48) not null comment '主需求',
  content varchar(1024) not null comment '主需求内容',
  created datetime not null comment '创建时间'
);
